from itsdangerous import URLSafeTimedSerializer, BadSignature
from aiogram import Router, F
from aiogram.types import Message, CallbackQuery
from aiogram.filters import Command
from config import SECRET_KEY
import aiohttp
from app.keyboards import main_kb, notifications

router = Router()

@router.message(Command("start"))
async def start_command(message: Message):
    telegram_id = message.from_user.id

    async with aiohttp.ClientSession() as session:
        async with session.get(
            f"http://server:8000/api/check_telegram_link/{telegram_id}"
        ) as response:
            if response.status == 200:
                user_data = await response.json()
                if user_data.get("telegram_id") == telegram_id:
                    await message.answer(f"–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –æ–±—Ä–∞—Ç–Ω–æ, {user_data['username']}üëãüèª!", reply_markup=main_kb)
                    return

    args = message.text.split(maxsplit=1)[1] if len(message.text.split()) > 1 else None

    if not args:
        await message.answer("–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å! –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∞–≤—Ç–æ—Ä–∏–∑—É–π—Ç–µ—Å—å —á–µ—Ä–µ–∑ —Å–∞–π—Ç, —á—Ç–æ–±—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –±–æ—Ç–∞.")
        return

    serializer = URLSafeTimedSerializer(SECRET_KEY)
    try:
        user_id = serializer.loads(args)
    except BadSignature:
        await message.answer("–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π —Ç–æ–∫–µ–Ω. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.")
        return

    async with aiohttp.ClientSession() as session:
        async with session.post(
            "http://server:8000/api/link_telegram",
            json={"user_id": user_id, "telegram_id": telegram_id},
        ) as response:
            if response.status == 200:
                await message.answer("–í—ã —É—Å–ø–µ—à–Ω–æ –ø—Ä–∏–≤—è–∑–∞–ª–∏ Telegram –∫ —Å–≤–æ–µ–º—É –∞–∫–∫–∞—É–Ω—Ç—É!",reply_markup=main_kb)
            else:
                await message.answer("–û—à–∏–±–∫–∞ –ø—Ä–∏–≤—è–∑–∫–∏. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.")

